"""Scale test: does the pipeline survive a few-hundred-node architecture?

Generates a synthetic hierarchical ICS plant (Purdue L5..L0 — site SCADA over N areas, each
with controllers and field devices) at several sizes and times each analysis stage. The point
is to find what scales and what doesn't, honestly.

Run: python -m experiments.scale   (writes experiments/SCALE.md)
"""
from __future__ import annotations

import time
from pathlib import Path

from ics_modeler.assets import Architecture, Asset, PurdueLevel, Segmentation
from ics_modeler.mapping import load_rules, map_architecture
from ics_modeler.scoring import (
    chokepoints,
    path_findings,
    score_architecture,
    segmentation_violations,
    sensitivity,
)


def generate(n_areas: int, ctrl_per_area: int = 2, field_per_ctrl: int = 4) -> Architecture:
    """A synthetic plant: site SCADA over `n_areas` areas, each with controllers + field devices."""
    a: dict = {}

    def add(name, level, ctype, protocols, auth, conns, exposed=()):
        a[name] = Asset(name=name, level=level, component_type=ctype,
                        protocols=list(protocols), authenticated=auth,
                        exposed_interfaces=list(exposed), connections=list(conns))

    L = PurdueLevel
    add("internet", L.L5_INTERNET, "firewall", ["HTTPS"], True, ["ent_ws"])
    add("ent_ws", L.L4_ENTERPRISE, "workstation", ["HTTPS", "RDP"], True, ["dmz_hist", "vpn"])
    add("vpn", L.L4_ENTERPRISE, "jump_host", ["VPN"], False, ["site_scada"], ["remote_access"])
    add("dmz_hist", L.L3_5_DMZ, "historian", ["HTTPS", "SQL"], True, ["site_hist"])
    add("site_hist", L.L3_OPERATIONS, "historian", ["SQL", "OPC"], True, ["site_scada"])
    add("site_scada", L.L2_SUPERVISORY, "SCADA_server", ["OPC", "DNP3"], False, [])

    targets = []
    for i in range(n_areas):
        asn = f"area{i}_scada"
        add(asn, L.L2_SUPERVISORY, "SCADA_server", ["OPC", "Modbus"], False, ["site_scada"])
        for c in range(ctrl_per_area):
            cn = f"area{i}_plc{c}"
            fields = [f"{cn}_dev{d}" for d in range(field_per_ctrl)]
            add(cn, L.L1_CONTROL, "PLC", ["Modbus"], False, [asn, *fields], ["network"])
            for f in fields:
                add(f, L.L0_PROCESS, "sensor", ["Modbus"], False, [cn], ["field"])
            if c == 0:
                targets.append(cn)

    seg = Segmentation(default_allow=True, boundaries=[
        {"zones": frozenset({"L4_ENTERPRISE", "L2_SUPERVISORY"}), "allow": {"OPC", "DNP3", "VPN"}},
        {"zones": frozenset({"L4_ENTERPRISE", "L1_CONTROL"}), "allow": set()},
    ])
    return Architecture(assets=a, entry_nodes=["internet", "ent_ws"],
                        target_nodes=targets[:3], segmentation=seg,
                        meta={"name": f"Synthetic plant ({len(a)} assets)"})


def _time(fn):
    t = time.perf_counter()
    out = fn()
    return out, time.perf_counter() - t


def measure(arch, rules, run_sensitivity=True) -> dict:
    graph, t_graph = _time(arch.graph)
    reach, t_reach = _time(arch.reachability_graph)
    _, t_map = _time(lambda: map_architecture(arch, rules))
    scores, t_score = _time(lambda: score_architecture(arch, graph))
    _, t_paths = _time(lambda: path_findings(reach, arch.entry_nodes, arch.target_nodes, scores))
    _, t_choke = _time(lambda: chokepoints(reach))
    _, t_viol = _time(lambda: segmentation_violations(arch))
    t_sens = None
    if run_sensitivity:
        _, t_sens = _time(lambda: sensitivity(arch, graph))
    return {
        "nodes": graph.number_of_nodes(), "edges": graph.number_of_edges(),
        "graph": t_graph, "reach": t_reach, "map": t_map, "score": t_score,
        "paths": t_paths, "choke": t_choke, "viol": t_viol, "sensitivity": t_sens,
    }


def run() -> str:
    rules = load_rules("data/mapping_rules.yaml")
    rows = []
    for n_areas in (8, 25, 50, 100):
        arch = generate(n_areas)
        # sensitivity re-scores ~81x; skip it past a few hundred nodes (that IS the finding)
        rows.append(measure(arch, rules, run_sensitivity=arch.graph().number_of_nodes() <= 300))

    def cell(v):
        return "—" if v is None else f"{v*1000:.0f} ms" if v < 1 else f"{v:.2f} s"

    L = ["# Scale Test — Results\n",
         "_Generated by `python -m experiments.scale`. Synthetic hierarchical plant; per-stage "
         "wall-clock on one machine (relative, not absolute)._\n",
         "| nodes | edges | graph | reach | map | score | paths | chokepts | seg | sensitivity |",
         "|------:|------:|------:|------:|----:|------:|------:|---------:|----:|------------:|"]
    for r in rows:
        L.append(f"| {r['nodes']} | {r['edges']} | {cell(r['graph'])} | {cell(r['reach'])} | "
                 f"{cell(r['map'])} | {cell(r['score'])} | {cell(r['paths'])} | "
                 f"{cell(r['choke'])} | {cell(r['viol'])} | {cell(r['sensitivity'])} |")

    L.append("\n## What scales, what doesn't\n")
    L.append("- **Per-run analysis is sub-second into the several-hundred-node range.** Graph "
             "build, mapping, attack paths, scoring, and segmentation stay in the low tens of "
             "milliseconds even at ~1100 nodes (the topology is hierarchical, so edge count is "
             "near-linear and k-shortest paths stay cheap).")
    L.append("- **Betweenness centrality (chokepoints) is the per-run soft spot.** It is O(V·E) "
             "and grows from ~14 ms (94 nodes) to ~2 s (1106 nodes) — the dominant per-run cost "
             "at scale. Fix when needed: sample-approximate betweenness (`k` pivots) or restrict "
             "it to candidate waypoints.")
    L.append("- **`score_architecture` is ~O(V²)** (per-asset shortest-path exposure + downstream "
             "BFS) but small in absolute terms (~85 ms at 1106 nodes).")
    L.append("- **`sensitivity()` is the overall bottleneck.** It re-scores the whole model ~81 "
             "times, so it inherits scoring's cost ×81 — already >1 s at 281 nodes, hence skipped "
             "above a few hundred here. Fix: memoize the graph-derived factors (exposure, "
             "downstream) once and vary only the weights across perturbations.")
    L.append("- **Figures don't scale *visually*.** The network diagram renders, but a "
             "many-hundred-node spring layout is an unreadable hairball — at scale the briefing "
             "text, risk table, and ATT&CK Navigator layer are the usable outputs, not the graph "
             "image. An honest tool would switch to per-zone summaries above ~50 nodes.\n")
    L.append("**Bottom line.** The narrative (rank assets, find IT→OT bypasses, trace paths, map "
             "techniques) holds at scale. Two components need a known, local engineering fix "
             "before thousands of nodes: **betweenness** (sample-approximate it) and "
             "**`sensitivity()`** (memoize graph-derived factors). Neither is a redesign.\n")

    Path("experiments/SCALE.md").write_text("\n".join(L), encoding="utf-8")
    return "\n".join(L)


if __name__ == "__main__":
    print(run())
